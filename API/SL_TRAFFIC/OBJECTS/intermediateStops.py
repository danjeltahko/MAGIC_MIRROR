class IntermediateStops():

    def __init__(self, name, departureTime_planned, departureTime_realTime, arrivalTime_planned, arrivalTime_realTime, latitude, longitude) -> None:
        self.name = name
        self.departureTime_planned = departureTime_planned
        self.departureTime_realTime = departureTime_realTime
        self.arrivalTime_planned = arrivalTime_planned
        self.arrivalTime_realTime = arrivalTime_realTime
        self.latitude = latitude
        self.longitude = longitude